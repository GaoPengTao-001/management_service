package com.org.management.entity;

public class ValueQuery {

    private String busId;

    private String timeDimension;

    private String dateValue;

    private String regionId;

    private String fmcId;

    private String ascCd;

    public String getBusId() {
        return busId;
    }

    public void setBusId(String busId) {
        this.busId = busId;
    }

    public String getTimeDimension() {
        return timeDimension;
    }

    public void setTimeDimension(String timeDimension) {
        this.timeDimension = timeDimension;
    }

    public String getDateValue() {
        return dateValue;
    }

    public void setDateValue(String dateValue) {
        this.dateValue = dateValue;
    }

    public String getRegionId() {
        return regionId;
    }

    public void setRegionId(String regionId) {
        this.regionId = regionId;
    }

    public String getFmcId() {
        return fmcId;
    }

    public void setFmcId(String fmcId) {
        this.fmcId = fmcId;
    }

    public String getAscCd() {
        return ascCd;
    }

    public void setAscCd(String ascCd) {
        this.ascCd = ascCd;
    }
}
